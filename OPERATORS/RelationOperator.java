public class RelationOperator {
    /*
    
    RELATIONAL operator is used to check the realtion between two or more operator 
    it check whether that particular value is greater or less then that. or equal to
    it will return boolean value as result:>  true or false.
    
======================================================
    > : greather than
    >= : greather than equal to 
    < : smaller than
    <= : smaller than equal to 
    == : equal to
======================================================

    */
    public static void main(String[] args) {
        System.out.println(12>5);  //     true
        System.out.println(5>=5);  //     true 
        System.out.println(2>5);  //      false
        System.out.println(1>=1);  //     true
        System.out.println(15==15);  //   true
    }
}
